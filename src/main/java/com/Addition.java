package com;

public class Addition {
	
	public int add(int a, int b)
	{
		return a+b;
	}
	
	public int substract(int a, int b)
	{
		return a-b;
	}
	
	public int division(int a, int b)
	{
		return a/b;
	}
	public int multiply(int a, int b)
	{
		return a*b;
	}
	public String concat(String a, String b)
	{
		String c= "";
		c=c+a;
		c=c+b;
		return c;
	}
	
}
